// Ternary Operator Example
let score = 85;
let grade =
  score >= 90
    ? "A+"
    : score >= 80
    ? "A"
    : score >= 70
    ? "B"
    : score >= 60
    ? "c"
    : score >= 50
    ? "D"
    : "F";
console.log(grade);
